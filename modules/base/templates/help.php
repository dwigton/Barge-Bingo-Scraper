<h2>Using this Site</h2>
<ol>
    <li>
        <h3>Casting a vote</h3>
        <p>To vote you must post your vote in &gt;letter&gt;-&lt;number&gt; format on 
        <a href='http://forum.nasaspaceflight.com/index.php?topic=36559.0'>Nasaspaceflight.com</a>.
        The case of the letter does not matter and any non alphanumeric letter can come between the letter and number. 
        Examples a1, A1, a-1, A 1, and so on.</p>
    </li>
    <li>
        <h3>Checking your square</h3>
        <p>Simply move your mouse cursor over the square and look at the lower left, you will see the owner of squares change as you pass over them.
        You can also click on your square to get a complete vote history for that square.</p>
    </li>
    <li>
        <h3>Viewing your personal vote history</h3>
        <p>In the "username" search box above, type in your forum username. 
        Click search or hit enter after selecting your name. 
        This will show you every post you made that looks like a vote.</p>
    </li>
    <li>
        <h3>Why you did not get your square</h3>
        <p>Use the step above to view your vote history. This site will list the reasons you did not get your vote. Generally there are 3 reasons.</p>
        <ol>
            <li>You have already voted for another square.</li>
            <li>You edited your post after voting.</li>
            <li>The square you wanted was taken by a previous poster</li>
        </ol>
        <p>In all these cases links are provided to the posts in question so you can verify for yourself that the program is not making things up.
        If you find an error please PM danielw on the Nasaspaceflight forums.</p>
    </li>
</ol>
